/// Spacing tokens used for padding, margins, gaps, and layout dimensions.
///
/// Prefer these semantic steps over one-off numeric values so an application's
/// spacing scale can be changed centrally.
abstract final class AppSpacing {
  static const double space2xs = 4;
  static const double spaceXs = 8;
  static const double spaceSm = 12;
  static const double spaceMd = 16;
  static const double spaceLg = 24;
  static const double spaceXl = 32;
  static const double space2xl = 40;
  static const double space3xl = 48;
  static const double space4xl = 64;
  static const double space5xl = 80;
  static const double space6xl = 96;
}